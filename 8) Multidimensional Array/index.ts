const singleDimensionalArray: number[] = [1, 2, 3, 4, 5];
const multiDimensionalArray: number[][] = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];
const tripleDimensionalArray: number[][][] = [[[1, 2], [3, 4]], [[5, 6], [7, 8]]];

console.log("Single Dimensional Array:", singleDimensionalArray);
console.log("Multi Dimensional Array:", multiDimensionalArray);
console.log("Triple Dimensional Array:", tripleDimensionalArray);

